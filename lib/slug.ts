export function toSlug(text: string): string {
  return text
    .toLowerCase()
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-');
}

export function barSlug(nombre: string, ciudad: string): string {
  return `${toSlug(nombre)}-${toSlug(ciudad)}`;
}
